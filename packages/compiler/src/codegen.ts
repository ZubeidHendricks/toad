import type { AgentAst, FieldDecl, PromptSegment, ToaType } from "./ast.js";

/**
 * Emit a readable, typed TypeScript module from an `AgentAst`. Output is
 * deterministic (same AST -> byte-identical code) and matches the contract in
 * `_bmad-output/architecture.md` §5. The emitted module imports its tools from
 * the co-located `<name>.tools` file and the loop from `@toa/runtime`.
 */
export function generate(ast: AgentAst): string {
  const pascal = pascalCase(ast.name);
  const inputType = `${pascal}Input`;
  const outputType = `${pascal}Output`;
  const hasOutputs = ast.outputs.length > 0;
  const hasTools = ast.tools.length > 0;
  const agentType = `Agent<${inputType}, ${hasOutputs ? outputType : "string"}>`;

  const lines: string[] = [];
  lines.push(`// Generated by toac from ${ast.name}.agent — do not edit.`);
  lines.push(`import { createAgent, type Agent } from "@toa/runtime";`);
  if (hasOutputs) {
    lines.push(`import { z } from "zod";`);
  }
  if (hasTools) {
    lines.push(
      `import { ${ast.tools.join(", ")} } from "./${ast.name}.tools";`,
    );
  }
  lines.push("");

  emitInterface(lines, inputType, ast.inputs);
  lines.push("");

  if (hasOutputs) {
    emitInterface(lines, outputType, ast.outputs);
    lines.push("");
    lines.push(`const outputSchema = z.object({`);
    for (const f of ast.outputs) {
      lines.push(`  ${f.name}: ${zodExpr(f.type)},`);
    }
    lines.push(`});`);
    lines.push("");
  }

  lines.push(`export const ${ast.name}: ${agentType} = createAgent({`);
  lines.push(`  name: ${JSON.stringify(ast.name)},`);
  lines.push(`  model: ${JSON.stringify(ast.model)},`);
  if (ast.description !== undefined) {
    lines.push(`  description: ${JSON.stringify(ast.description)},`);
  }
  if (hasTools) {
    lines.push(`  tools: { ${ast.tools.join(", ")} },`);
  }
  if (hasOutputs) {
    lines.push(`  outputSchema,`);
  }
  lines.push(`  prompt: (inputs: ${inputType}) =>`);
  lines.push(`    ${promptExpr(ast.prompt)},`);
  lines.push(`});`);
  lines.push("");
  lines.push(`export default ${ast.name};`);
  lines.push("");
  return lines.join("\n");
}

function emitInterface(
  lines: string[],
  name: string,
  fields: FieldDecl[],
): void {
  if (fields.length === 0) {
    lines.push(`export interface ${name} {}`);
    return;
  }
  lines.push(`export interface ${name} {`);
  for (const f of fields) {
    lines.push(`  ${f.name}: ${tsType(f.type)};`);
  }
  lines.push(`}`);
}

function tsType(t: ToaType): string {
  return t.array ? `${t.base}[]` : t.base;
}

function zodExpr(t: ToaType): string {
  const base = `z.${t.base}()`;
  return t.array ? `z.array(${base})` : base;
}

function promptExpr(segments: PromptSegment[]): string {
  return `\`${segmentsToBody(segments)}\``;
}

function segmentsToBody(segments: PromptSegment[]): string {
  let out = "";
  for (const seg of segments) {
    if (seg.kind === "text") {
      out += escapeTemplate(seg.value);
    } else if (seg.kind === "interp") {
      out += `\${${seg.path.join(".")}}`;
    } else if (seg.kind === "each") {
      out += `\${${seg.source.join(".")}.map((${seg.item}) => \`${segmentsToBody(seg.body)}\`).join("")}`;
    } else {
      const cond = `${seg.negate ? "!" : ""}${seg.cond.join(".")}`;
      out += `\${${cond} ? \`${segmentsToBody(seg.then)}\` : \`${segmentsToBody(seg.else)}\`}`;
    }
  }
  return out;
}

function escapeTemplate(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

function pascalCase(name: string): string {
  return name
    .split(/[_\s]+/)
    .filter((s) => s.length > 0)
    .map((s) => s[0]!.toUpperCase() + s.slice(1))
    .join("");
}
